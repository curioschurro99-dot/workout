export type Unit = "reps" | "sec" | "m" | "steps" | "min";

export interface Exercise {
  id: string;
  name: string;
  cat: "strength" | "cardio" | "mobility";
  muscle: string;
  sets: number;
  reps: number;
  note?: string;
  unit?: Unit;
}

export interface ProgramExercise {
  exId: string;
  sets: number;
  reps: number;
}

export interface ProgramDay {
  label: string;
  focus: string;
  exercises: ProgramExercise[];
}

export interface Program {
  id: string;
  name: string;
  desc: string;
  days: ProgramDay[];
}

export interface SessionLog {
  date: string;
  programId: string;
  dayLabel: string;
  focus: string;
  exCount: number;
}

export interface Goal {
  targetWeight: string;
  weeklyTarget: number;
  notes: string;
  startDate: string;
}

export interface Note {
  title: string;
  body: string;
}

export interface Pose {
  name: string;
  sanskrit: string;
  category: string;
  focus: string;
  difficulty: "Beginner" | "Intermediate" | "Advanced";
  hold: number;
  shape: string;
  seq: number | null;
  cue: string;
}

export interface WorkoutState {
  exercises: Exercise[];
  programs: Program[];
  activeProgramId: string | null;
  activeDayIndex: number;
  logs: SessionLog[];
  goal: Goal;
  myNotes: Note[];
  checks: Record<string, Record<number, boolean>>;
  videoUrl: string;
}

